# Программа переводит введенный текст в азбуку морзе

tabl_morze: dict = {
    'A': '.-', 'J': '.---', 'S': '...', '1': '.----',
    'B': '-...', 'K': '-.-', 'T': '-', '2': '..---',
    'C': '-.-.', 'L': '.-..', 'U': '..-', '3': '...--',
    'D': '-..', 'M': '--', 'V': '...-', '4': '....-',
    'E': '.', 'N': '-.', 'W': '.--', '5': '.....',
    'F': '..-.', 'O': '---', 'X': '-..-', '6': '-....',
    'G': '--.', 'P': '.--.', 'Y': '-.--', '7': '--...',
    'H': '....', 'Q': '--.-', 'Z': '--..', '8': '---..',
    'I': '..', 'R': '.-.', '0': '-----', '9': '----.'
}

line: str = input().upper()

for symbol in line:
    if symbol in tabl_morze:
        print(tabl_morze[symbol], end=' ')
